import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
        Scanner scan = new Scanner (System.in);
        int t = scan.nextInt();
        while(t-->0){
            double s = scan.nextDouble();
            double v = scan.nextDouble();
            System.out.println((2*s)/(3*v));
        }
	}
}

